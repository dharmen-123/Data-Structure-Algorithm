//    Two types of Arithmetic Operator 
// 1. Unary operator
// 2. Binary Operator

// // // Binary Operator // // //
// In Binary Operator we required two Operands with one Operator

// + Addition           2+5
// - Subtraction        8-3
// * Multiply           5*4
// / Division           10/2
// % Modulus            5%3

// Presedence of operator
// 1. {* , / , %}  left to right Associativity
// 2. {+ , -}  left to right Associativity

// // // Unary Operator // // //
// In unary operator we only required single operand and with operator

    // ++ Increment              Ex -> a=5
// 1. Post increment   
// Example  a++  , 
//  cout<<a++;  output -> 5
//  cout<<a;  output -> 6

// 2. Pre increment    
// Example  ++a
//  cout<<++a;  output -> 6
//  cout<<a;  output -> 6

    // -- Decrement              Ex -> b=7
// 1. Post decrement   
// Example  b--  , 
//  cout<<b--;  output -> 7
//  cout<<b;  output -> 6

// 2. Pre decrement    
// Example  --b
//  cout<<--b;  output -> 6
